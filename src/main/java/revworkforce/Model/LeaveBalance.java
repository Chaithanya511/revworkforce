package revworkforce.Model;


public class LeaveBalance {
    private String empId;
    private int cl;
    private int sl;
    private int pl;

    public String getEmpId() {
        return empId;
    }
    public void setEmpId(String empId) {
        this.empId = empId;
    }

    public int getCl() {
        return cl;
    }
    public void setCl(int cl) {
        this.cl = cl;
    }

    public int getSl() {
        return sl;
    }
    public void setSl(int sl) {
        this.sl = sl;
    }

    public int getPl() {
        return pl;
    }
    public void setPl(int pl) {
        this.pl = pl;
    }
}

